#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：只给出了上下标尺，其它均需要自己补充
***************************************************************************/
int main()
{
	int n;
	printf("请输入要打印的行数[3..13]\n");
	scanf("%d", &n);
	/* 上标尺 */
	printf("--------------------------------------------------------------------------------\n");
	printf("0         1         2         3         4         5         6         7         \n");
	printf("01234567890123456789012345678901234567890123456789012345678901234567890123456789\n");
	printf("--------------------------------------------------------------------------------\n");
    int triangle[n][n];

    for (int i = 0; i < n; i++) {
        triangle[i][0] = 1;
        triangle[i][i] = 1;
    }

    for (int i = 2; i < n; i++) {
        for (int j = 1; j < i; j++) {
            triangle[i][j] = triangle[i - 1][j - 1] + triangle[i - 1][j];
        }
    }


    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n - i - 1; j++) {
            printf("   ");
        }
        for (int j = 0; j <= i; j++) {
            printf("%6d", triangle[i][j]);
        }
        printf("\n");
    }

	/* 下标尺 */
	printf("--------------------------------------------------------------------------------\n");
	printf("01234567890123456789012345678901234567890123456789012345678901234567890123456789\n");
	printf("0         1         2         3         4         5         6         7         \n");
	printf("--------------------------------------------------------------------------------\n");
	printf("\n");

	return 0;
}


